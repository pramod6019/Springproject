package axela.inventory.model;

import java.io.Serializable;
import javax.persistence.*;


/**
 * The persistent class for the axela_inventory_batch database table.
 * 
 */
@Entity
@Table(name="axela_inventory_batch")
@NamedQuery(name="AxelaInventoryBatch.findAll", query="SELECT a FROM AxelaInventoryBatch a")
public class AxelaInventoryBatch implements Serializable {
	private static final long serialVersionUID = 1L;

	@Id
	@Column(name="batch_id")
	private int batchId;

	@Column(name="batch_active")
	private String batchActive;

	@Column(name="batch_desc")
	private String batchDesc;

	@Column(name="batch_entry_date")
	private String batchEntryDate;

	@Column(name="batch_entry_id")
	private int batchEntryId;

	@Column(name="batch_expiry_date")
	private String batchExpiryDate;

	@Column(name="batch_modified_date")
	private String batchModifiedDate;

	@Column(name="batch_modified_id")
	private int batchModifiedId;

	@Column(name="batch_no")
	private String batchNo;

	@Column(name="batch_time")
	private String batchTime;

	public AxelaInventoryBatch() {
	}

	public int getBatchId() {
		return this.batchId;
	}

	public void setBatchId(int batchId) {
		this.batchId = batchId;
	}

	public String getBatchActive() {
		return this.batchActive;
	}

	public void setBatchActive(String batchActive) {
		this.batchActive = batchActive;
	}

	public String getBatchDesc() {
		return this.batchDesc;
	}

	public void setBatchDesc(String batchDesc) {
		this.batchDesc = batchDesc;
	}

	public String getBatchEntryDate() {
		return this.batchEntryDate;
	}

	public void setBatchEntryDate(String batchEntryDate) {
		this.batchEntryDate = batchEntryDate;
	}

	public int getBatchEntryId() {
		return this.batchEntryId;
	}

	public void setBatchEntryId(int batchEntryId) {
		this.batchEntryId = batchEntryId;
	}

	public String getBatchExpiryDate() {
		return this.batchExpiryDate;
	}

	public void setBatchExpiryDate(String batchExpiryDate) {
		this.batchExpiryDate = batchExpiryDate;
	}

	public String getBatchModifiedDate() {
		return this.batchModifiedDate;
	}

	public void setBatchModifiedDate(String batchModifiedDate) {
		this.batchModifiedDate = batchModifiedDate;
	}

	public int getBatchModifiedId() {
		return this.batchModifiedId;
	}

	public void setBatchModifiedId(int batchModifiedId) {
		this.batchModifiedId = batchModifiedId;
	}

	public String getBatchNo() {
		return this.batchNo;
	}

	public void setBatchNo(String batchNo) {
		this.batchNo = batchNo;
	}

	public String getBatchTime() {
		return this.batchTime;
	}

	public void setBatchTime(String batchTime) {
		this.batchTime = batchTime;
	}

}